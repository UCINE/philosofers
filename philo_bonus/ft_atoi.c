#include "philo.h"

int	ft_atoi(const char *str)
{
	long			res;
	int				i;
	int				j;

	i = 0;
	j = 1;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			j *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		if ((res > 2147483647 && j == 1) || (res > 2147483648 && j == -1))
			exit(1);
		i++;
	}
	return (res * j);
}